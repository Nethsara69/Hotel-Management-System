    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(CusInfoTable.getValueAt(CusInfoTable.getSelectedRow(), 0).toString());
        try {
            Connection con = DBConnection.getconConnection();
            Statement st = con.createStatement();
            boolean b = st.execute("UPDATE customer SET Cus_Name='" + CusName.getText() + "',Cus_Address='" + CusAddress.getText() + "',Mobile='" + CusMobile.getText()+ "',Age='" + CusAge.getText() + "',Email='" + CusEmail.getText() + "',Gender='" + CusGender.getText() + "',NIC='" + CusNIC.getText() + "' Where CusID =" + id);
            if (!b) {
                setVisible(false);
                new CusInfo().setVisible(true);
            }
        } catch (Exception e) {
           JOptionPane.showMessageDialog(this, "Error");
        }
    }//GEN-LAST:event_UpdateButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(CusInfoTable.getValueAt(CusInfoTable.getSelectedRow(), 0).toString());
        try {
            Connection con = DBConnection.getconConnection();
            Statement st = con.createStatement();
            int r = JOptionPane.showConfirmDialog(this, "Are u sure delete selected row?");
            if (r == 0) {
                st.execute("DELETE FROM customer WHERE cusid=" + id);
                setVisible(false);
                new CusInfo().setVisible(true);
            }
        } catch (Exception e) {
            e.printStackTrace();

        }

    }//GEN-LAST:event_deleteButtonActionPerformed

