    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
         ResultSet resultSet = Select.getData("SELECT * FROM customer");
        DefaultTableModel model = (DefaultTableModel) CusInfoTable.getModel();
        try {
            while (resultSet.next()) {
                model.addRow(new Object[]{resultSet.getString(1), resultSet.getString(2), resultSet.getString(3), resultSet.getString(4), resultSet.getString(5), resultSet.getString(6), resultSet.getString(7), resultSet.getString(8)});
            }
            resultSet.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_formComponentShown

    private void CusInfoTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CusInfoTableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tbleModel = (DefaultTableModel) CusInfoTable.getModel();

        String ID = tbleModel.getValueAt(CusInfoTable.getSelectedRow(), 0).toString();
        String Name = tbleModel.getValueAt(CusInfoTable.getSelectedRow(), 1).toString();
        String Address = tbleModel.getValueAt(CusInfoTable.getSelectedRow(), 2).toString();
        String Mobile = tbleModel.getValueAt(CusInfoTable.getSelectedRow(), 3).toString();
        String Age = tbleModel.getValueAt(CusInfoTable.getSelectedRow(), 4).toString();
        String Email = tbleModel.getValueAt(CusInfoTable.getSelectedRow(), 5).toString();
        String Gender = tbleModel.getValueAt(CusInfoTable.getSelectedRow(), 6).toString();
        String NIC = tbleModel.getValueAt(CusInfoTable.getSelectedRow(), 7).toString();

        CusID.setText(ID);
        CusName.setText(Name);
        CusAddress.setText(Address);
        CusMobile.setText(Mobile);
        CusAge.setText(Age);
        CusEmail.setText(Email);
        CusGender.setText(Gender);
        CusNIC.setText(NIC);
    }//GEN-LAST:event_CusInfoTableMouseClicked

